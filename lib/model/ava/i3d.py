class I3D_Old(nn.Module):

    def __init__(self, num_classes=2, dropout_keep_prob = 1, input_channel = 3, spatial_squeeze=True):
        super(I3D_Old, self).__init__()
        self.features = nn.Sequential(
            BasicConv3d(input_channel, 64, kernel_size=7, stride=2, padding=3), # (64, L/2, 56, 56)
            nn.MaxPool3d(kernel_size=(1,3,3), stride=(1,2,2), padding=(0,1,1)),  # (64, L/2, 28, 28)
            BasicConv3d(64, 64, kernel_size=1, stride=1), # (64, L/2, 28, 28)
            BasicConv3d(64, 192, kernel_size=3, stride=1, padding=1),  # (192, L/2, 28, 28)
            nn.MaxPool3d(kernel_size=(1,3,3), stride=(1,2,2), padding=(0,1,1)),  # (192, L/2, 14, 14)
            Mixed_3b(), # (256, L/2, 14, 14)
            Mixed_3c(), # (480, L/2, 14, 14)
            nn.MaxPool3d(kernel_size=(3, 3, 3), stride=(2, 2, 2), padding=(1, 1, 1)), # (480, L/4, 7, 7)
            Mixed_4b(),# (512,  L/4, 7, 7)
            Mixed_4c(),# (512,  L/4, 7, 7)
            Mixed_4d(),# (512,  L/4, 7, 7)
            Mixed_4e(),# (528,  L/4, 7, 7)
            Mixed_4f(),# (832,  L/4, 7, 7)   ------------>RPN

            nn.MaxPool3d(kernel_size=(2,1,1),stride=(2,1,1),padding=(0,0,0)),# (832,  L/8, 3, 3)
            Mixed_5b(),# (832, L/8, 7, 7)
            Mixed_5c(),# (1024, L/8, 7, 7)

            nn.AvgPool3d(kernel_size=(2,7,7),stride=1),# (1024, L/16, 1, 1)
            nn.Dropout3d(dropout_keep_prob),# (1024, L/16, 1, 1)
            nn.Conv3d(1024,num_classes,kernel_size=1,stride=1,bias=True)# (num_class, L/16, 1, 1)
        )

        self.spatial_squeeze = spatial_squeeze
        self.softmax = nn.Softmax()

    def forward(self, x):
        logits = self.features(x)
        print("logits {}".format(logits.shape)) # [b, class, L/16, 1, 1])
        if self.spatial_squeeze:
            logits = logits.squeeze(3)
            logits = logits.squeeze(3)
        print("logits {}".format(logits.shape)) # ([2, 80, 7])

        averaged_logits = torch.mean(logits,2) # ([2, 80])
        predictions = self.softmax(averaged_logits) # ([2, 80])

        print("averaged_logits {}".format(averaged_logits.shape))
        print("predictions {}".format(predictions.shape))

        return predictions,averaged_logits

